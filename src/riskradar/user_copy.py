def movement_result(key: str, direction: str) -> str:
    """키와 방향(up/down/flat)의 사용자 결과 문구를 반환한다."""
    return MOVEMENT_RESULTS.get(key, {}).get(direction, "현재 결과를 설명할 문구가 없습니다.")




def movement_label(key: str, direction: str) -> str:
    """표의 첫 열에 쓰는 짧은 방향 표현."""
    if key in {"BREAKEVEN", "T10Y3M"}:
        return {"up": "커지면", "down": "줄어들면", "flat": "큰 변화 없음"}.get(direction, direction)
    return {"up": "오르면", "down": "내리면", "flat": "큰 변화 없음"}.get(direction, direction)


def movement_result_cell(key: str, direction: str) -> str:
    """방향 라벨을 따로 보여주는 표·목록용 결과 문구."""
    text = movement_result(key, direction)
    for prefix in ("오르면 ", "내리면 ", "큰 변화가 없으면 ", "커지면 ", "줄어들면 "):
        if text.startswith(prefix):
            return text[len(prefix):]
    return text

def indicator_summary(key: str) -> str:
    return INDICATOR_SUMMARIES.get(key, "이 지표의 현재 값과 최근 움직임을 봅니다.")


def indicator_caution(key: str) -> str:
    return INDICATOR_CAUTIONS.get(key, "")


def render_movement_table(key: str, heading: str = "## 움직임별 결과") -> str:
    """지표 방향별 결과를 2열 표로 렌더링한다."""
    rows = MOVEMENT_RESULTS.get(key)
    if not rows:
        return ""
    return "\n".join([
        heading,
        "",
        "| 움직임 | 결과적으로 볼 수 있는 변화 |",
        "|---|---|",
        f"| {movement_label(key, 'up')} | {movement_result_cell(key, 'up')} |",
        f"| {movement_label(key, 'down')} | {movement_result_cell(key, 'down')} |",
        f"| {movement_label(key, 'flat')} | {movement_result_cell(key, 'flat')} |",
    ])


def render_static_explainer(key: str, name: str) -> str:
    """설명 탭에서 공통으로 쓰는 짧은 지표 설명."""
    parts = [f"# {name}", "", indicator_summary(key)]
    table = render_movement_table(key)
    if table:
        parts += ["", table]
    caution = indicator_caution(key)
    if caution:
        parts += ["", f"> **참고:** {caution}"]
    return "\n".join(parts)
