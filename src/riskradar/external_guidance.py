GUIDANCE_BY_KEY: dict[str, tuple[ExternalGuide, ...]] = {
    "VIX": (FOMC, EMPLOYMENT),
    "HYOAS": (EMPLOYMENT, FOMC),
    "T10Y3M": (FOMC, EMPLOYMENT),
    "DGS30": (INFLATION, TREASURY, FOMC),
    "DGS2": (FOMC, EMPLOYMENT, INFLATION),
    "DFII10": (INFLATION, FOMC, TREASURY),
}


def render_external_guidance(key: str) -> str:
    guides = GUIDANCE_BY_KEY.get(key, ())
    if not guides:
        return ""
    lines = [
        "## 앱 밖에서 확인하면 좋은 발표·뉴스",
        "",
        "일정이나 뉴스를 자동으로 읽는 기능은 아닙니다. 현재 지표를 이해할 때 **무엇을 보고, 결과에 따라 생각을 어떻게 바꿀지** 알려주는 참고 설명입니다.",
    ]
    for guide in guides:
        lines += [
            "",
            f"### {guide.title}",
            f"**왜 보나:** {guide.why}",
            "",
            "**무엇을 볼까**",
        ]
        lines += [f"- {item}" for item in guide.watch]
        lines += ["", "**결과에 따라 이렇게 읽습니다**"]
        lines += [f"- **{condition}:** {meaning}" for condition, meaning in guide.branches]
        lines += ["", f"> **한계:** {guide.caveat}"]
    return plain_language("\n".join(lines))
